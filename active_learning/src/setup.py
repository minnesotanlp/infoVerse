import argparse
import torch
import random
import numpy as np
from transformers import glue_processors as processors
from transformers import (
    AutoModelWithLMHead,
    AutoModelForSequenceClassification,
    BertForMultipleChoice,
    AutoTokenizer,
    AutoConfig
)
from src.data import processors, output_modes


MODELS = {
    "lm":AutoModelWithLMHead,
    "sc":AutoModelForSequenceClassification,
    "mc":BertForMultipleChoice
}

def set_seed(args):
    random.seed(args.seed)
    np.random.seed(args.seed)
    torch.manual_seed(args.seed)
    if args.n_gpu > 0:
        torch.cuda.manual_seed_all(args.seed)

def load_model(args):
    args.model_type = args.model_type.lower()
    if args.head=="sc" or args.head =="mc":
        processor = processors[args.task_name]()
        args.output_mode = output_modes[args.task_name]
        label_list = processor.get_labels()
        num_labels = len(label_list)
        config = AutoConfig.from_pretrained(
            args.config_name if args.config_name else args.model_name_or_path,
            num_labels=num_labels,
            finetuning_task=args.task_name,
            cache_dir=args.cache_dir if args.cache_dir else None,
        )
    else:
        config = AutoConfig.from_pretrained(
            args.config_name if args.config_name else args.model_name_or_path,
            cache_dir=args.cache_dir if args.cache_dir else None
        )
    tokenizer = AutoTokenizer.from_pretrained(
        args.tokenizer_name if args.tokenizer_name else args.model_name_or_path,
        do_lower_case=args.do_lower_case,
        cache_dir=args.cache_dir if args.cache_dir else None,
    )
    tokenizer_class = AutoTokenizer
    model_class = MODELS[args.head]
    model = model_class.from_pretrained(
        args.model_name_or_path,
        from_tf=bool(".ckpt" in args.model_name_or_path),
        config=config,
        cache_dir=args.cache_dir if args.cache_dir else None,
    )
    model.to(args.device)
    return model, tokenizer, model_class, tokenizer_class

